// Helper functions
export function getMaterialById(id: string): ExtractedMaterial | undefined {
  return MATERIALS.find(m => m.id === id);
}

export function getMaterialsByCategory(category: MaterialCategory): ExtractedMaterial[] {
  return MATERIALS.filter(m => m.category === category);
}

export function getMaterialsBySubcategory(subcategory: MaterialSubcategory): ExtractedMaterial[] {
  return MATERIALS.filter(m => m.subcategory === subcategory);
}

export function getCriticalMaterials(minRating: number = 4): ExtractedMaterial[] {
  return MATERIALS.filter(m => (m.criticalityRating || 0) >= minRating);
}

